#include "ParticleGenerator.h"

ParticleGenerator::ParticleGenerator(Shader shader, Texture2D texture, GLuint amount)
	: shader(shader), texture(texture), amount(amount)
{
	this->init();
}

void ParticleGenerator::Update(GLfloat dt, MovingSceneObject &object, GLuint newParticles, glm::vec2 offset)
{
	// Add new particles 
	for (GLuint i = 0; i < newParticles; ++i)
	{
		int unusedParticle = this->firstUnusedParticle();
		this->respawnParticle(this->particles[unusedParticle], object, offset);
	}
	// Update all particles
	for (GLuint i = 0; i < this->amount; ++i)
	{
		Particle &p = this->particles[i];
		p.setLife(p.getLife()- dt); // reduce life
		if (p.getLife() > 0.0f)
		{	// particle is alive, thus update
			p.setPosition(p.getPosition() - p.getVelocity() * (dt));
			p.setTransparency(p.getTransparency() - dt * 2.5);
		}
	}
}

// Render all particles
void ParticleGenerator::Draw()
{
	// Use additive blending to give it a 'glow' effect
	glBlendFunc(GL_SRC_ALPHA, GL_ONE);
	this->shader.Use();
	for (Particle particle : this->particles)
	{
		if (particle.getLife() > 0.0f)
		{
			this->shader.SetVector2f("offset", particle.getPosition());
			this->shader.SetVector4f("color", glm::vec4(particle.getColor(), particle.getTransparency()));
			this->texture.Bind();
			glBindVertexArray(this->VAO);
			glDrawArraysInstanced(GL_TRIANGLES, 0, 6, this->particles.size());
			glBindVertexArray(0);
		}
	}
	// Don't forget to reset to default blending mode
	glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
}

